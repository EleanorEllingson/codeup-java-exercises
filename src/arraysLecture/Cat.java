package arraysLecture;

public class Cat {
    private String name1;
    private int age;

    public Cat(String name, int age) {
        this.name1 = name1;
        this.age = age;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getName1() {
        return name1;
    }

    public void setName1(String name) {
        this.name1 = name;
    }

    public Cat(int age) {
        this.age = age;
    }
}
